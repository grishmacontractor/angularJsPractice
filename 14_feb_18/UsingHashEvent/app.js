window.addEventListener('hashchange',function () {
    if(window.location.hash === '#/line/50')
    {
        console.log("hash on Line 50")
    }

    if(window.location.hash === '#/line/60')
    {
        console.log("hash on Line 60")
    }

    if(window.location.hash === '#/line/70')
    {
        console.log("hash on Line 70")
    }
})